package br.com.trainingloiane.javabasico.exercicios.java.intermediario.aula76.classe;

public class Concatenacao {
    public static void main(String[] args) {
        String curso = "Curso";
        String java = "Java";

        String concatena = curso+java;

        System.out.println("Primeira forma de se concatenar: " + concatena);
        System.out.println("Segunda forma de se concatenar: " + curso+" "+java);
        System.out.println("Terceira forma de se concatenar: " + curso+java);


    }
}
